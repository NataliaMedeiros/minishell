#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*new;
	size_t	total;

	total = nmemb * size;
	if (nmemb && (total / nmemb) != size)
	{
		return (NULL);
	}
	if ((long) nmemb < 0 || (long) size < 0)
		return (0);
	new = malloc(nmemb * size);
	if (new == NULL)
		return (NULL);
	ft_bzero(new, nmemb * size);
	return (new);
}
